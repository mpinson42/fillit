#include "fillit.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*s1;

	s1 = (unsigned char *)s;
	while (n > 0)
	{
		n--;
		s1[n] = c;
	}
	return (s);
}

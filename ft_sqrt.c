int		ft_sqrt(int n)
{
	int nb;

	nb = 0;
	if (n <= 0)
		return (0);
	while (nb <= n)
	{
		if ((nb * nb) == n)
			return (nb);
		nb++;
	}
	return (0);
}

int		ft_round(float nb)
{
	return (nb >= 0 ? (int)(nb + 0.5) : (int)(nb - 0.5));
}
